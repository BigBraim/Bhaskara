    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        delta txts = new delta();
        
        txts.setA(Integer.parseInt(txtA.getText()));
        txts.setB(Integer.parseInt(txtB.getText()));
        txts.setC(Integer.parseInt(txtC.getText()));
        
        txts.valordelta();
        txts.denominador();
        txts.numeradorx1();
        txts.numeradorx2();
        int delta1 = (int) txts.valordelta();
        int result = (int) txts.x1();
        int resulta = (int) txts.x2();
        
        if (txts.valordelta()!=0 && txts.valordelta()>0){
        txtX1.setText(Integer.toString(result));
        txtX2.setText(Integer.toString(resulta));
        delta.setText(Integer.toString(delta1));
        }else{
            JOptionPane.showMessageDialog(this, "Nao existe raiz");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

